#!/bin/bash
IP=$1
echo Probing $IP
echo ________________________________________
echo 		host UP ?
echo ________________________________________
nmap -sP $IP 2> /dev/null \
    | grep ^Host
echo ________________________________________
echo 		TCP NMAP
echo ________________________________________
nmap -p 1-65535 -T4 -A -v $IP
echo ________________________________________
echo 		UDP NMAP
echo ________________________________________
nmap -p 1-65535 -sU -T4 $IP
echo ________________________________________
echo 		OS NMAP
echo ________________________________________
nmap -sT -O -Pn $IP 
echo ________________________________________
echo 		OS xProbe2
echo ________________________________________
xprobe2 $IP
echo ________________________________________
echo 		PING
echo ________________________________________
ping $IP -c 3
echo ________________________________________
echo 		HPING TIMESTAMP
echo ________________________________________
hping3 $IP -S --tcp-timestamp -c 3